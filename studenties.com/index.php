																							<span class="uMZCuOaaxIxu">Keep in mind here that I’m describing the theory and thinking behind why people would try this—not what I currently believe or what is necessarily true.

The idea behind adopting the Uberman sleep schedule is that REM sleep is the only stage of sleep that actually matters (that’s when your body is recovering and healing itself). Since we typically only spend about 20% of our time asleep in REM, the goal is to cut out the remaining 80% and “hack” your body to gain the full benefit of several hours of sleep in only a fraction of the time. In a normal adult, it takes somewhere around ninety minutes after you fall asleep to enter your first REM cycle; this means that once you start only taking twenty to forty minute naps every few hours, you will be getting no REM sleep at all, and you will become sleep deprived. Eventually your body will be desperate enough that it will start entering REM sleep as soon as your head hits the pillow for your naps. The theory is that after a few weeks or months of acclimation, your body should completely adapt to these new instant-REM naps. If you’re doing twenty minutes every three hours, that adds up to a total of over two and a half hours of REM sleep per day—a typical seven-hour-a-night sleeper spending 20% of their time in REM is getting less than an hour and a half. You’d actually be getting more REM during your 160 minutes per day of sleep than someone sleeping seven hours.

This surplus of REM sleep is attributed by people who claim to be living on this schedule to feeling more awake and alive than ever—sharper wits, clearer minds, and significantly increased productivity beyond what would be expected by just having a few extra hours awake each day. Other purported benefits are that your dreams are richer, more vivid, and often lucid (meaning you can consciously control them while they are occurring). Basically everything is awesome. Why would anyone not want to do this?

Well, if you saw the Seinfeld episode, you might have a few ideas why.</span>
																							<p>
	The deployment of cashless technology in Asia still faces the challenge of working across multiple countries and adhering to local regulations, and while the growth in digital wallets in Asia has been phenomenal, only a handful are able to provide users with services in more than one Asian country, let alone on an international basis.

</p>
																							<p>
	But far from being a reason for pessimism, the region's high cash use shows that the most exciting developments in digital payments are still ahead of us.

</p>
																							<p>
	New solutions are coming online all the time. Recently, HSBC became the first foreign bank in China to offer customers the ability to pay overseas tuition fees via smartphone. Upcoming innovations include financial institutions delivering credit cards directly to a customer's digital wallet once approval has been given, with no need to wait for a card to arrive in the post.

</p>
																							<p>Being in the most dynamic region in the world for digital payments can also be a source of opportunity. Companies and service providers with a strong foothold in the Asia-Pacific are perfectly positioned to export their technology and capture a global lead, as the challenges of developing systems that can work across borders and currencies are solved.</p>
																							<div class="vZhpdFBPAd" style="background: red;">I understand the interest in these sleep routines, and in trying to make the most of every hour in the day. I’ll continue to watch with interest as these sleep patterns are subjected to more scientific study. Ultimately, there are no real short-cuts to healthy sleep. The smartest time management strategy is to focus on getting the high-quality sleep you need at night, and taking full advantage of all the physical, emotional, and cognitive power that your night sleep gives you during your waking day.</div>
																							<div class="XiwPHQStYwhZ">At the time I’m writing this, it’s been almost eighteen years since the Seinfeld season finale, and nearly twenty years since the Friar’s Club episode where Kramer tried to emulate Leonardo da Vinci’s alleged habit of only sleeping twenty minutes at a time every three hours. I’m sure I saw it when it first aired, but I was still in high school and probably didn’t give it much thought at the time. That all changed a few years later. I occasionally see the subject of polyphasic sleep and the “Uberman” sleep schedule crop up, so I’m going to share my personal experience with it.

I met my future wife online while I was in college. This was still in the early days of the Internet, so dating sites and social networks like Facebook didn’t exist yet—if you wanted to meet people online, you either used newsgroups, web forums, or IRC. We met in an IRC channel used for out-of-character socialization between members of an email-based Star Trek role playing simulation. Yeah—it doesn’t get much geekier than that, right? She was the captain of her own ship, and I was a civilian bartender (like a male version of Whoopi Goldberg’s character on TNG), and our in-game flirting slowly migrated out into real-life flirting. I was still in Canada and she lived in Florida at the time, which made a relationship difficult, but we managed. To make a long story short, soon after I graduated from college we got engaged and I was on my way to Florida with a fiance visa.

The thing about fiance visas is that they didn’t (and maybe still don’t) come with a work permit—it could take anywhere up to a year after moving to the US to file all the necessary paperwork, wait for responses, file additional paperwork, get your fingerprints scanned, and pay various fees before you can legally get a job; and the whole time you’re waiting, you’re not allowed to leave the country or else everything is voided and you have to start over from scratch (including getting a new visa before being allowed back in—a process that could also take up to a year or longer). Suffice it to say, I had a lot of free time on my hands, and no time-sensitive commitments like a job to hold me down. I spent a lot of my time working on personal projects to sharpen my programming skills, and helping my father-in-law (off the books) with his software business. It was during this time that one of my friends (who was living with me and my wife at the time) reminded me of Kramer’s comically failed attempt at the odd sleeping schedule; he told me that it was a real thing, and pointed out that I was in a perfect situation to try it out. After a little bit of research, I completely agreed.</div>
