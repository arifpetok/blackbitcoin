																							<span class="vYcsKBuYrUJu">These may sound like new approaches to sleep—but there are scientists and historians who think that forms of polyphasic and biphasic sleep patterns have an ancient history, and were once the norm for human sleep. Research over the past couple of decades suggests that our ancestors, for most of human history, may have relied on biphasic sleep patterns. The shift to the 8-hour night of sleep may have been a relatively recent development, one that was spurred on by industrialization and widespread access to electricity and artificial light. Some scientific findings suggest that our ancestors sleep patterns may have included a stint of sleep lasting 3-5 hours, followed by a period of time awake, engaging in some light activity, followed by another stretch of 3-5 hours of sleep. 

If you imagine the daily patterns of life in a pre-industrial, pre-electric age, this pattern makes some sense. Living closely in sync with natural daylight, the first phase of sleep would have likely come with the onset of darkness. That stretch of darkness, lasting 12 hours or more, would have been a long time to sleep without interruption. Waking partway through, to have something to eat, have sex, or engage is some other light activity, could likely have been common practice, before falling back to sleep before waking at dawn. 

These segmented sleep patterns may indeed have a long history among humans—but does that mean they make sense for us today? Sleep is influenced by many factors, some biological, and others social. Switching to a segmented sleep routine is no simple matter—and for most people, it’s not practical or advisable. </span>
