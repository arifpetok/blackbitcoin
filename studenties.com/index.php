Biphasic and polyphasic sleep: what you need to know
I can’t go so far as to recommend a biphasic or polyphasic sleep routine, generally or for any particular group of people. I’m just not convinced, at this point, that these alternate sleep routines can deliver the quantity and quality of sleep people need on a regular basis.</div>
																							<div class="GMAHfMzGzhNc">Everybody is busy these days, looking to squeeze as much as they can out of every moment of the day, and night. Sure, you know you need roughly eight hours of sleep—but wouldn’t it be great if you could somehow get that ‘ideal eight’ in five hours? (Hint: it’s possible, but not too healthy.)

I have patients who are interested in bio-hacking their sleep, whether to maximize their time efficiency, or to boost performance, or both. This often leads to a discussion of biphasic and polyphasic sleep, and whether it’s a good idea to try. 

Not familiar with polyphasic sleep? You’re in good company. As you’re about to learn, although these sleep patterns may have an ancient history, they’re kind of new all over again, as people look for ways to maximize the efficiency of their rest. It’s a good time to take closer look at these sleep patterns—what science tells us about their history, how people are using them today, and what risks are associated with them. 

What are biphasic and polyphasic sleep? 
These are patterns of sleep that involve sleep in segments, rather than in a single, uninterrupted block of time. Think about the sleep pattern most of us keep: a stretch of 7-8 hours a night asleep, followed by a long, usually unbroken stretch of wakefulness of about 16 hours, before dropping back into sleep. That sleep pattern, so common in the United States and much of the industrialized world, is known as “monophasic sleep.” 

Biphasic and polyphasic sleep break up that single period of sleep into segments of rest that occur at different times throughout the day and night. 

Biphasic sleep divides sleep into two parts. It may involve a long stretch of sleep overnight, with a much shorter block of sleep during the day. A 6-hour nightly sleep period might be paired with a 30-90 minute nap during the day. 

Biphasic sleep is sometimes known as “siesta sleep,” a nod to some Western cultures, including Spain and Greece, that continue to incorporate the afternoon rest as part of their cultures and commonly-held sleep patterns. 

Polyphasic sleep breaks up sleep into more than two episodes throughout the day and night. Polyphasic sleepers might sleep as many as six times a day, in some combination of 2-3 hour blocks of sleep and 30-90 minute naps, which over 24 hours are meant to add up to the equivalent of a full night of rest. </div>
